		<h3>Strengths</h3>

		<ol>
			<li>Shared funding, resources, and collection materials with the Lucy Desi Museum.</li>
			<li>Full size viewing theatre for community events and viewing parties.</li>
			<li>Staff: knowledgeable, courteous, excellent customer service.</li>
			<li>Streaming service provides extensive access to resources and a source of funding for the library and its projects.</li>
		</ol>
		<h3>Weaknesses</h3>
		
		<ol>
			<li>Licensing for streaming of Lucille Ball television shows and movies will require negotiation and contracts with copyright holders.</li>
			<li>Cost of updating and maintaining technologies such as: computers, monitors, scanners, movie screen etc.</li>
			<li>Limited collection specific to Lucille Ball.</li>
			<li>Maintenance for the older library building.</li>
		</ol>
		
		<h3>Opportunities</h3>

		<ol>
			<li>Fellowship opportunities for film and television students in nearby schools such as UCLA and USC.</li>
			<li>Expand collection to include other actors and comedians.</li>
			<li>Further expansion of community collaborations and partnerships for visibility and shared resources.</li>
		</ol>
		
		<h3>Threats</h3>
		
		<ol>
			<li>Lack of security on premises to avoid possible theft.</li>
			<li>Accessibility to location/traffic.</li>
			<li>Restrictive copyright and licensing agreements.</li>
			<li>Rising maintenance and operations fees.</li>
		</ol>
		
