		<h3 id="technology">Technology</h3>
		
		<p>The Lucille Ball Library will utilize a variety of office equipment and information technology equipment in its daily operations and provide excellent service to patrons.</p>
		
		<p>Lucille Ball Library staff utilizes an internal email server that allows all employees to contact each other within the system in a safe and secure closed server. Staff also employs virtual meeting services to conduct department meetings and check-in meetings. Staff will also utilize a staff blog to update each other on assignments and projects. This system will minimize downtime and allow employees to access project information remotely without having to have one-on-one meetings (Westbrook, 2012).</p>

		<p>The Lucille Ball Library features a full-size viewing theater where patrons can view episodes from “I Love Lucy” and other films starring Lucille Ball. According to the U.S. Census (2010) report, over 88% of households in the Los Angeles area all have one computer and of those same households, 78.3% have broadband internet.  In order to capitalize on this demographic, the Lucille Ball Library provides streaming of all “I Love Lucy” episodes as well as other Lucille Ball films and television shows. This streaming service functions similarly to Netflix or Hulu, but instead of charging patrons a monthly fee, patrons will login with their library card and be allotted a daily allowance of viewing hours. Once the allotted hours are exhausted, users will be given the option to purchase more hours for a small fee or can purchase a Lucy Desi Museum membership for a yearly cost ranging from $50 to $5,000 (National Comedy Center, n.d.). The Lucy Desi Museum membership will allow patrons to have unlimited viewing hours for streaming as well as unlimited admission to the museum and a 15% discount on all gift shop purchases (National Comedy Center, n.d.).</p>
		
