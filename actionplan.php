		<h3 id="description">Description</h3>

		<p>“Virtual reality is an important technology that continues to evolve, grow, and integrate into the lives of our library users and libraries can offer access to these technologies to create unique learning and experiential opportunities for users” (Pope, 2018). The Lucille Ball Library will leverage the surge in VR use and integration across libraries by retrofitting a bookmobile into a mobile VR station. This mobile VR station will be called ‘The Lucille Ball Experience’ and will satisfy three goals.</p>
			
			<ul>
				<li>Virtual Reality viewing of popular I Love Lucy episodes and the Lucille Ball Comedy Festival (Whetsell, 2018). The use of VR allows users an immersive experience that is free from distractions of everyday, which increases retention of information and makes users feel as if they are part of the scene instead of just watching a scene (Bond, 2018). Through the use of VR headsets, patrons will be able to interact with I Love Lucy episodes by being able to experience the show from the various characters viewpoints, explore sets, and can even reenact scenes as their favorite character. By digitally recreating each episode and utilizing all video footage, computers will be able to accurately modernize and reproduce key episodes of the show to take full advantage of VR technology. Each year the Lucy-Desi Museum hosts the Lucille Ball Comedy Festival in Jamestown, New York, however given that the Lucille Ball Library is located in Chatsworth, California; many patrons may not be able to experience the festival first hand. By having museum staff outfitted with 360° cameras that rove around the festival and having stereoscopic cameras situated around the venue and stage (Waniata, 2018), patrons will be able to explore the festival and watch performances free from distraction and without having to book a flight to New York.</li>
				<li><img src="images/vrtruck.jpg" alt="Mobile VR Truck Inside"></li>
				<li>Virtual reality browsing and interacting with the Lucille Ball Library exhibits and the Lucy-Desi Museum exhibits (Lucy-Desi, 2015). VR has become a growing trend among museums around the world and has been utilized to overcome physical, economic, and accessibility barriers. The National Museum of Finland and Kramer Museum have both been on the cutting edge of implementing VR by showcasing over 70 artifacts all strictly in VR and allowing patrons to virtually enter a painting and interact with subjects in the painting (Coats, 2018). By utilizing 360° cameras to record objects patrons will be able to interact with current exhibits at the Lucille Ball Library and the Lucy-Desi Museum. Stereoscopic cameras will allow patrons to not only move around objects, but could also pick them up and move them around.</li>
				<li>The Lucille Ball Experience will serve as a mobile billboard and mobile extension of the Lucille Ball Library. According to a recent study by transportation analytic company Inrix, “Los Angeles drivers spent an average of 102 hours in traffic during peak times” (Angelenos, 2018) and according to the Department of Motor Vehicles there are over 7,762,453 registered vehicles in Los Angeles County (Los Angeles Almanac, 2018). By having the Lucille Ball Experience vehicle vinyl wrapped with the library’s logo, contact information, and social media links; the library is able to advertise to a captive audience on route to various locations through Los Angeles. Additionally by having a mobile VR station the library is able to bring the library to patrons who are unable to come to Chatsworth. Gavin Wolterjer (2018), writer for the Public Library Association, outlines the importance of bookmobiles to promote libraries, create a mobile community space, and provide access to information and technology that impoverished communities may not get to experience. The Lucille Ball Experience will also frequent various colleges in to promote the Lucille Ball Library and foster community partnerships with campuses.</li>
