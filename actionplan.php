		<h3 id="budgets">Budget</h3>
		
		<p>Funding will be taken directly from the $26 million annual budget of the Lucy-Desi Museum as well as grants from outside investors and donors. We will also explore funding from Chatsworth City Council. The purchase and customization of the VR station vehicle is anticipated to be the most significant cost at an estimated $500,000 (Waite, 2018). The VR program development cost to take advantage of the Oculus Rift and HTC Vive VR headsets is $200,000 (Levski, n.d.). The VR station will utilize one Oculus Rift VR headset and one HTC Vive headset which combined cost $1000. By having both platforms the VR station is guaranteed to be able to play all VR programs without having to choose between one platform and another. In order to run the headsets we will purchase two Origin Neuron desktop computers for $2000 (Buzzi, 2018). In order to have patrons see what VR users are seeing will purchase four TCL 4K televisions for $900 (Best, n.d.). We will also purchase a VR camera that will allow us to digitize current and future objects, and the Lucille Ball Comedy Festival for $17,000 (Mooovr, n.d.). An estimated $12,000 will be utilized for marketing of the Lucille Ball Experience and $64,000 will be used for two employees to operate the mobile VR station. The total required budget needed to successfully implement the Lucille Ball Experience is $796,900.</p>
		
		<h3 id="evaluation">Evaluation</h3>

		<p>The Lucille Ball Library staff will continue to monitor the VR station to determine value and any changes that need to make to better serve our patrons. The public relations manager will monitor patron feedback by having users fill surveys and comment cards. Staff will also report back on patron verbal feedback and provide analytics of uses and users. The VR program will also keep track of which exhibits users frequented and what episodes received the most traffic. After four months of monitoring, the public relations manager will prepare and present a findings report to the library director and the Lucy-Desi Museum board of directors. Additionally all departments will prepare a SWOT analysis of the Lucille Ball Experience and that will determine what can be improved and how to continue progressing the VR station.</p>
		
