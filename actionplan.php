		<h2>Virtual Reality Action Plan</h2>
		
		<p> We will utilize our virtual reality (VR) and video streaming capabilities to promote the library to surrounding communities by developing a mobile VR station that will allow users to interact with the collection virtually and also double as an advertising platform for the library.</p>
		
